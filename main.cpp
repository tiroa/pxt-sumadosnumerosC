/**
 * Esta función recibe dos números y devuelve el resultado de la suma entre ambos
 */
//% weight=100 color=#0fbc11 icon="\uf1ec"
namespace sumadosnumerosC {
    //% blockId="Sumador"
    //% block
    int sumando(int num1, int num2){
        int resultado = num1 + num2;
        return resultado;
    }
}